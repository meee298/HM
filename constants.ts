export const getStoredMovies = (): Movie[] => {
  try {
    const stored = localStorage.getItem(STORAGE_KEY);
    // If key exists (even if empty array), return it. 
    // Only return mock data if key is null (first visit).
    if (stored !== null) {
      return JSON.parse(stored);
    }
  } catch (e) {
    console.error('Failed to load movies', e);
  }
  // Initialize with mock data only on very first run
  saveStoredMovies(MOCK_MOVIES);
  return MOCK_MOVIES;
};

export const saveStoredMovies = (movies: Movie[]) => {
  try {
    localStorage.setItem(STORAGE_KEY, JSON.stringify(movies));
  } catch (e) {
    console.error('Failed to save movies', e);
  }
};

export const getWatchlist = (): string[] => {
  try {
    return JSON.parse(localStorage.getItem(WATCHLIST_KEY) || '[]');
  } catch {
    return [];
  }
};

export const toggleWatchlist = (id: string): boolean => {
  try {
    const list = getWatchlist();
    let newList;
    let added = false;
    
    if (list.includes(id)) {
      newList = list.filter(item => item !== id);
    } else {
      newList = [...list, id];
      added = true;
    }
    
    localStorage.setItem(WATCHLIST_KEY, JSON.stringify(newList));
    return added;
  } catch {
    return false;
  }
};

